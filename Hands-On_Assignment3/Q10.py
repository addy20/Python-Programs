marks = float(input("Enter your mark: "))

if marks >= 90:
    print("Grade: O")
elif marks >= 80:
    print("Grade: A")
elif marks >= 70:
    print("Grade: B")
elif marks >= 60:
    print("Grade: C")
elif marks >= 50:
    print("Grade: D")
elif marks >= 40:
    print("Grade: E")
else:
    print("Grade: F")